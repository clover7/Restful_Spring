package com.restful.test.model;

public class Measure {

	int seq;
	String key;
	String values;
	String extends_etc;
	String input_dt;
	
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	} 
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}	
	public String getValues() {
		return values;
	}
	public void setValues(String values) {
		this.values = values;
	}
	public String getExtends_etc() {
		return extends_etc;
	}
	public void setExtends_etc(String extends_etc) {
		this.extends_etc = extends_etc;
	}
	public String getInput_dt() {
		return input_dt;
	}
	public void setInput_dt(String input_dt) {
		this.input_dt = input_dt;
	}
	
}
